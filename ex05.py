#5) Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas diferentes. Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. Seu objetivo é descobrir qual interruptor controla qual lâmpada. Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada?  

#Entendendo que há 3 salas cada um com uma lâmapda:

#Primeira ida:
# - Ligue o interruptor 1 e deixe-o ligado por cerca de 5 minutos. Depois, desligue o interruptor 1 e ligue interruptor . Em seguida, vá até uma das salas com as lâmpadas.

#Na sala:

#Se a lâmpada estiver acesa, ela está conectada ao interruptor 2 (o interruptor que você acabou de ligar).
#Se a lâmpada estiver apagada, mas quente ao toque, ela está conectada ao interruptor 1 (que estava ligado por um tempo e depois desligado).
#Se a lâmpada estiver apagada e fria, ela está conectada ao interruptor 3 (que nunca foi ligado).

#Segunda ida:

#Com a informação obtida, você já sabe a qual interruptor uma lâmpada está conectada. Agora, ligue o interruptor que você ainda não verificou e mantenha-o ligado, enquanto desliga o outro.
#Vá até a segunda sala restante e observe a lâmpada para determinar a qual interruptor ela pertence.

